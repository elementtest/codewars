/*
input: array of increasing letters from a
output: missing letter in the array
i.e. 
['a', 'b', 'c', 'e']
output 'd'
also this is case sensitive... so 
i.e. 
['D', 'E', 'F', 'H']
output 'G'
*/

function findMissingLetter(array) {
    return array.charCodeAt();
}

console.log(findMissingLetter(['a','b','c','d']));
